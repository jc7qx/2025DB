BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "Author" (
	"pNo"	char(6) NOT NULL,
	"name"	varchar(15) NOT NULL DEFAULT '',
	PRIMARY KEY("pNo","name")
);
CREATE TABLE IF NOT EXISTS "Browse" (
	"mId"	CHAR(8) NOT NULL DEFAULT 'a0910001',
	"pNo"	CHAR(6) NOT NULL,
	"browseTime"	TEXT NOT NULL,
	PRIMARY KEY("mId","pNo","browseTime"),
	FOREIGN KEY("mId") REFERENCES "Member"("mId") ON DELETE SET DEFAULT ON UPDATE CASCADE,
	FOREIGN KEY("pNo") REFERENCES "Product"("pNo")
);
CREATE TABLE IF NOT EXISTS "Cart" (
	"mId"	char(8) NOT NULL,
	"cartTime"	TEXT NOT NULL DEFAULT ' ',
	"tNo"	char(5) NOT NULL,
	PRIMARY KEY("mId","cartTime")
);
CREATE TABLE IF NOT EXISTS "Member" (
	"mId"	TEXT NOT NULL,
	"pId"	TEXT NOT NULL,
	"name"	TEXT NOT NULL,
	"birthday"	TEXT DEFAULT NULL,
	"phone"	TEXT DEFAULT NULL,
	"address"	TEXT DEFAULT NULL,
	"email"	TEXT DEFAULT NULL,
	"introducer"	TEXT DEFAULT NULL,
	PRIMARY KEY("mId")
);
CREATE TABLE IF NOT EXISTS "Orders" (
	"pNo"	char(6) NOT NULL,
	"mId"	char(15) NOT NULL,
	"cartTime"	text NOT NULL DEFAULT '0000-00-00 00:00:00',
	"amount"	int(11) DEFAULT '0'
);
CREATE TABLE IF NOT EXISTS "Product" (
	"pNo"	CHAR(6) NOT NULL,
	"pName"	VARCHAR(30),
	"unitPrice"	DECIMAL(10, 2),
	"category"	VARCHAR(20),
	PRIMARY KEY("pNo"),
	CONSTRAINT "UnitPrice_Check" CHECK("unitPrice" > 100)
);
CREATE TABLE IF NOT EXISTS "Record" (
	"tNo"	char(5) NOT NULL,
	"pNo"	char(6) NOT NULL,
	"salePrice"	decimal(10, 2) DEFAULT NULL,
	"amount"	INTEGER DEFAULT NULL,
	PRIMARY KEY("tNo","pNo"),
	FOREIGN KEY("tNo") REFERENCES "Transactions"("tNo")
);
CREATE TABLE IF NOT EXISTS "Transactions" (
	"tNo"	CHAR(5) NOT NULL,
	"transMid"	CHAR(8) NOT NULL,
	"transTime"	TEXT NOT NULL,
	"method"	VARCHAR(5) NOT NULL,
	"bankId"	VARCHAR(14),
	"bankName"	VARCHAR(20),
	"cardId"	VARCHAR(10),
	"cardType"	VARCHAR(10),
	"dueDate"	TEXT,
	PRIMARY KEY("tNo"),
	FOREIGN KEY("transMid") REFERENCES "Member"("mId")
);
